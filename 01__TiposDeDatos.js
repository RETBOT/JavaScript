/*
 * Tipos de datos 
*/

// Numero  - number
console.log(10);
console.log(10.24);

// Texto - string
console.log("RET");
console.log('BOT');
console.log(`Hola, soy RETBOT`);

// logicos - booleanos
console.log(true);
console.log(false);

// indefinido - undefined 
console.log(undefined);

// nulo - null 
console.log(null);

/* 
* Tipos de datos estructurales
*/
// objeto - object
console.log({
  nombre: "RETBOT",
  edad: 22,
  fechaDeNachimiento: null,
  esAlto: true,
  ciudades: ["Quito", "Guayaqui", "Buenos Aires"],
  direccion: { callePrincipal: "Siempre viva" },
});

// Tipos de datos de tipo lista - array
console.log(['Mexico','Venezuela','Ecuadro']);

console.log([21, true, {nombre:'RETBOT'},[1,2,3,4,5]]);